									<br><br>
									En cuanto al producto, la promoción debe ser diseñada de manera que resalte las características y beneficios que ofrece, y de esta manera poder crear una imagen positiva en la mente del consumidor. La promoción puede utilizarse también para presentar nuevos productos o variantes del producto actual.
