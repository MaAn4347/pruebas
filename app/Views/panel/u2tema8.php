									Por ejemplo, un minorista puede ayudar a promocionar los productos de una empresa al colocarlos en lugares destacados en su tienda, en su sitio web o en su catálogo. Un mayorista puede ayudar a promocionar los productos de una empresa al incluirlos en su lista de productos y ofrecerlos a otros minoristas.
